package calculatorAssignment;


  public class calculator {
	
	  	double a, b ;
		calculator(double a, double b) {
		this.a=a;
		this.b=b;
		}
		final double add() {
			return a+b;
		}
		final double sub() {
			return a-b;
		}
		 final double mul() {
			return a*b;
		}
		final double div() {
			return a/b;
		}
 }
	
